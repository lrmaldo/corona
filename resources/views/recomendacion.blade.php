                <p>Los coronavirus son una familia de virus que causan enfermedades que van desde el resfriado común hasta enfermedades respiratorias más graves, circulan entre humanos y animales. A veces, los coronavirus que infectan a los animales pueden evolucionar, transmitirse a las personas y convertirse en una nueva cepa de coronavirus capaz de provocar enfermedades en los seres humanos, tal y como sucedió con el Síndrome&nbsp;Respiratorio Agudo&nbsp;Severo&nbsp;(<strong><a href="https://www.cdc.gov/sars/about/fs-sars-sp.html" target="_blank"><span style="color:#0000FF">SARS</span></a></strong>), en Asia en febrero de 2003 y, el&nbsp;Síndrome Respiratorio de Oriente Medio&nbsp;(<strong><a href="https://www.who.int/features/qa/mers-cov/es/" target="_blank"><span style="color:#0000FF">MERS-CoV</span></a></strong>), que fue detectado por primera vez en Arabia Saudita en 2012.</p>
                
                <p>Los coronavirus humanos comúnmente causan padecimientos leves a moderados en personas de todo el mundo. Sin embargo, los dos coronavirus humanos antes mencionados, MERS-CoV y SARS-CoV, causan enfermedad grave con mayor frecuencia.</p>
                
                <h3><strong>¿QUÉ PODEMOS HACER PARA PROTEGERNOS?</strong></h3>
                
                <p>Aunado a las acciones que el sector salud realiza, la población juega un papel importante para&nbsp;reducir la probabilidad de exposición y transmisión&nbsp;del virus, por ello debemos realizar las medidas de higiene personal y del entorno.</p>
                
                <p>De ahí la importancia de que todas las personas&nbsp;<strong>conviertan en hábito las medidas que comprenden sencillos procedimientos de higiene</strong>, tanto personales como del entorno, los cuales comúnmente se realizan en los diferentes lugares donde desarrollan sus actividades cotidianas.</p>
                
                <ul>
                    <li><strong>Lavarse las manos</strong>&nbsp;frecuentemente&nbsp;<strong>con agua y jabón</strong>&nbsp;o utilizar soluciones a base de alcohol gel al 70%.</li>
                    <li><strong>Al toser o estornudar,&nbsp;</strong>utilizar el estornudo de etiqueta<strong>, que consiste en cubrirse la nariz y boca con un pañuelo desechable o con el ángulo interno del brazo</strong>.</li>
                    <li><strong>No escupir</strong>. Si es necesario hacerlo, utilizar un pañuelo desechable, meterlo en una bolsa de plástico, anudarla y tirarla a la basura; después lavarse las manos.</li>
                    <li><strong>No tocarse la cara con las manos sucias</strong>, sobre todo nariz, boca y ojos.</li>
                    <li><strong>Limpiar y desinfectar superficies y objetos de uso común</strong>&nbsp;en casas, oficinas, sitios cerrados, transporte, centros de reunión, etc., ventilar y permitir la entrada de luz solar.</li>
                    <li><strong>Quedarse en casa cuando se tienen enfermedades respiratorias</strong>&nbsp;y acudir al médico si se presenta alguno de los síntomas (fiebre mayor a 38° C, dolor de cabeza, dolor de garganta, escurrimiento nasal, etc.).</li>
                    <li><strong>Evitar</strong>&nbsp;en lo posible&nbsp;<strong>contacto con personas que tengan enfermedades respiratorias</strong></li>
                </ul>
                
